    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String phoneNumber = jTextField2.getText();
        String email = jTextField3.getText();
        String city = jTextField4.getText();
        String address = jTextField5.getText();
        
        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Моля, попълнете всички полета!", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String phonePattern = "^[0-9]{10}$";
        if (!phoneNumber.matches(phonePattern)) {
            JOptionPane.showMessageDialog(this, "Невалиден телефонен номер!", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String emailPattern = "^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$";
        if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(this, "Невалиден имейл адрес!", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (city.isBlank() || address.isBlank()) {
            JOptionPane.showMessageDialog(this, "Моля, попълнете всички полета!", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String id = jLabel6.getText();

        query.update(new String[] { "Name", "Telephone", "Email", "City", "Address" }, "Employees",
                new String[] { name, phoneNumber, email, city, address }, "EmployeeID", id);
        
        JOptionPane.showMessageDialog(this, "Успешно обновен!");
    }

    private void jList1MouseReleased(java.awt.event.MouseEvent evt) {
        String selectedRow = jList1.getSelectedValue();
        String[] tokens = selectedRow.split(" ");
        String employeeId = tokens[0];

        String[] columnsNames = { "EmployeeID", "Name", "Telephone", "Email", "City", "Address" };
        ArrayList<String> data = query.selectWhere(columnsNames, "Employees", "EmployeeId", employeeId);
        String[] employee = data.get(0).split("---");

        jLabel6.setText(employee[0]);
        jTextField1.setText(employee[1]);
        jTextField2.setText(employee[2]);
        jTextField3.setText(employee[3]);
        jTextField4.setText(employee[4]);
        jTextField5.setText(employee[5]);
    }

